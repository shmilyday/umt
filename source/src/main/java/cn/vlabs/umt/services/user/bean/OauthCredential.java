package cn.vlabs.umt.services.user.bean;

import cn.vlabs.umt.services.user.Credential;

public class OauthCredential implements Credential{
	private String clientId;
	private String userName;
	private String password;
	public OauthCredential(){};
	public OauthCredential(String clientId,String userName,String password){
		this.clientId=clientId;
		this.userName=userName;
		this.password=password;
	}
	public String getClientId() {
		return clientId;
	}
	public void setClientId(String clientId) {
		this.clientId = clientId;
	}
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	

}
